#[test]
fn test_delete_node_modules() {
    use std::fs;

    use crate::dir::delete_node_modules;

    use tempfile::tempdir;

    // Create a temporary directory for testing
    let temp_dir = tempdir().expect("Failed to create temporary directory");

    // Create a "node_modules" directory within the temporary directory
    let node_modules_dir = temp_dir.path().join("node_modules");
    fs::create_dir(&node_modules_dir).expect("Failed to create node_modules directory");

    // Call the function under test
    delete_node_modules(temp_dir.path().to_str().unwrap(), 42);

    // Check that the "node_modules" directory is deleted
    assert!(!node_modules_dir.exists());
}
